#include "Vec3.h"



Vec3::Vec3()
{
}

void Vec3::SetX(double _x)
{
	x = _x;
}
double Vec3::GetX()
{
	return x;
}

void Vec3::SetY(double _y)
{
	y = _y;
}
double Vec3::GetY()
{
	return y;
}

void Vec3::SetZ(double _z)
{
	z = _z;
}
double Vec3::GetZ()
{
	return z;
}
